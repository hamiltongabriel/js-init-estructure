// Usando Else If Para varios Scripts
const value = 5;
if (value === 5) {
  console.log('1');
} else if (value === 2) {
  console.log('2');
} else {
  console.log('Nenhum dos valores');
}

// Switch
const styleValue = 3;
switch (styleValue) {
  case 1:
    console.log('1');
    break;
  case 2:
    console.log('2');
    break;
  case 3:
    console.log('3');
    break;
  default:
    console.log('Nenhum dos valores');
}
